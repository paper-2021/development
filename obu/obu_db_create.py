        cur.execute("CREATE TABLE IF NOT EXISTS RSU (link_id INTEGER PRIMARY KEY AUTOINCREMENT, start INTEGER, end INTEGER, start_latitude TEXT, start_longitude TEXT, end_latitude TEXT, end_longitude TEXT)")
